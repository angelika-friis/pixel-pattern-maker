export function createColorSymbols(pixelColors: PixelColor[]): PixelColorSymbol[] {
  return pixelColors.map((color, index) => ({
    ...color,
    symbol: COLOR_SYMBOLS[index] ?? String(index + 1),
  }));
}

export function createColorSymbolMap(pixelColors: PixelColorSymbol[]) {
  return new Map(pixelColors.map((color) => [color.hex, color.symbol]));
}
